package fairway.javabasic.Dictionary;

public class Lan {

    private String getEng;
    private String getMm;

    public String getGetEng() {
        return getEng;
    }

    public void setGetEng(String getEng) {
        this.getEng = getEng;
    }

    public String getGetMm() {
        return getMm;
    }

    public void setGetMm(String getMm) {
        this.getMm = getMm;
    }

    public Lan(String getEng, String getMm) {
        this.getEng = getEng;
        this.getMm = getMm;
    }
}
